<?php
							//				  	二进制		     十进制
	$x = 1021; 			 	// 			 	  	  1111111101	 	 1021
	$y = 15; 			 	 // 				  0000001111	  	   15
	/* 基本的位运算 */
	$z = $x & $y; 			 //位与 			  0000001101	 	   13
	$z = $x | $y; 			 //位或 		 	  1111111111	 	 1023
	$z = $x ^ $y; 			 //位异或 		 	  1111110010	 	 1010
	/* 位移运算 */
	$z = $x << 1; 			 //位左移操作		 11111111010	 	 2042
	$z = $x << 2; 			 //位左移操作		111111110100	 	 4084
	$z = $x >> 1; 			 //位右移操作 		   111111110	 	  510
	$z = $x >> 2; 			 //位右移操作 		    11111111	 	  255
?>
